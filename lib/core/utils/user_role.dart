/// Defines user roles used for access control in the Fetosense MIS application.
///
/// The [UserRoles] class provides static constants representing different user roles
/// such as user, admin, and super admin.
class UserRoles {
  /// Regular user role.
  static const String user = 'user';

  /// Admin user role.
  static const String admin = 'admin';

  /// Super admin user role.
  static const String superAdmin = 'super_admin';
}
